"""写作 Agent 的 Prompt：生成章节正文、生成摘要。"""

WRITE_SYSTEM = """你是一位才华横溢的中文小说作家，文风细腻、节奏感强、善于描写动作与心理。
你的任务是根据给定的大纲、设定和前情，写出高质量的小说正文。

写作准则：
1. 严格遵循"本章写作计划"的情节节拍，但可以丰富细节、增加场景感
2. 多用"展示"而非"陈述"——通过动作、对话、感官细节来呈现，少用概括性陈述
3. 对话要符合人物性格，推动情节，避免信息倾倒式独白
4. 场景转换自然，节奏张弛有度
5. 章末要有钩子或情绪余韵，吸引读者继续
6. 只输出正文，不要写"第X章"标题（系统会加），不要写作者备注、不要解释
7. 不要分点、不要列大纲，直接成段叙述
8. 避免陈词滥调和过度修饰，语言要有作者辨识度"""


def write_chapter_prompt(context: str, word_target: int) -> list[tuple[str, str]]:
    return [
        (
            "user",
            f"""请根据以下背景资料，写出本章正文。

{context}

【字数要求】约 {word_target} 字（中文字符）。允许 ±20% 浮动，但绝不能少于 {int(word_target * 0.8)} 字。
请直接输出正文，从第一段开始，不要任何前言、标题或说明。""",
        )
    ]


def summarize_chapter_prompt(
    chapter_id: str, title: str, content: str
) -> list[tuple[str, str]]:
    return [
        (
            "user",
            f"""请把下面这章小说压缩成一份【前情提要摘要】，供后续章节参考。

【第 {chapter_id} 章】{title}
【正文】
{content}

要求：
- 用 150-250 字概括本章发生的核心事件、关键转折、人物状态变化
- 只记事实与结果，不要文学性描写
- 重点保留"对后续情节有影响"的信息（新登场人物、新设定、伏笔、关系变化）

直接输出摘要正文，不要前后缀。""",
        )
    ]
